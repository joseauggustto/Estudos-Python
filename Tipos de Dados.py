#Aqui está uma lista dos possíveis tipos de dados em Python: 

#Inteiro
inteiro = 10
print(type(inteiro)) 

#Float
decimal = 10.5
print(type(decimal))

#String
string = 'José Augusto'
print(type(string))

#Booleano 
boleano = True
print(type(boleano))

#Set (???) Coleção não ordenada de elementos únicos.
set_var = {1, 2, 3}
print(type(set_var))

#Dicionário (Coleção de pares chave-valor)
Dicionário = {'nome': 'José', 'idade': 30}
print(type(Dicionário))

#Tupla (Coleção ordenada e imutável)
Tupla = (1, 2, 3)
print(type(Tupla))

#Range (Sequência de números)
range = range(10)
print(type(range))

#Lista (Coleção ordenada e mutável)
Lista = [1, 2, 3]
print(type(Lista))

#NoneType
NoneType = None
print(type(NoneType))

